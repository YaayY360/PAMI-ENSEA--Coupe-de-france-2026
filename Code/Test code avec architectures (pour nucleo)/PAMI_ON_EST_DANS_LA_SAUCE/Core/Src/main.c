/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  uint32_t us_GetTick() {
	  return __HAL_TIM_GET_COUNTER(&htim6);
  }
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM6_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);
  HAL_TIM_Base_Start(&htim6);
  /////////////////////////////////////// Config sens moteurs //////////////////////////////////////
  HAL_GPIO_WritePin(IN1_Port, IN1_Pin, 0); HAL_GPIO_WritePin(IN4_Port, IN4_Pin, 0);
  HAL_GPIO_WritePin(IN2_Port, IN2_Pin, 0); HAL_GPIO_WritePin(IN3_Port, IN3_Pin, 0);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  /////////////////////////////////////// Acquisition des capteurs //////////////////////////////////////
	  detection_ligne = (HAL_GPIO_ReadPin(X4_Port, X4_Pin) << 0) |
					    (HAL_GPIO_ReadPin(X3_Port, X3_Pin) << 1) |
					    (HAL_GPIO_ReadPin(X1_Port, X1_Pin) << 2) |
		      		    (HAL_GPIO_ReadPin(X2_Port, X2_Pin) << 3) ;
	  ///////////////////////////////////////////// Test des cas ////////////////////////////////////////////
	  switch(detection_ligne) {
	  case HORS_PISTE:     M1 = M2 = 0;    break;
	  case SUR_PISTE:      M1 = M2 = 1;    break;
	  case TOURNER_DROITE: M1 = 1; M2 = 0; break;
	  case TOURNER_GAUCHE: M1 = 0; M2 = 1; break;
	  }
	  /////////////////////////////// Génération des signaux pour les moteurs ///////////////////////////////
	  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, M1*VITESSE_MOTEUR); // Moteur de droite
	  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, M2*VITESSE_MOTEUR); // Moteur de gauche
	  ///////////////////////////////////////////// Partie TRIG /////////////////////////////////////////////
	  switch(TRIG) {
	  case TRIG_BAS:
		  if((us_GetTick() - switchTRIG) > 60000) { // 60 ms pour l'état haut
			  HAL_GPIO_WritePin(TRIG_Port, TRIG_Pin, 1);
			  switchTRIG = us_GetTick();
			  TRIG = TRIG_HAUT;
		  }
		  break;
	  case TRIG_HAUT:
		  if((us_GetTick() - switchTRIG) > 10) { // 10 us pour l'état bas
			  HAL_GPIO_WritePin(TRIG_Port, TRIG_Pin, 0);
			  switchTRIG = us_GetTick();
			  TRIG = TRIG_BAS;
		  }
		  break;
	  }
	  /////////////////////////////////////////// Test de distance //////////////////////////////////////////
	  if(distance < 5) M1 = M2 = 0;
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

